# Исходный список
my_list = [42, 69, 322, 13, 0, 99, -5, 9, 8, 7, -6, 5]

# Как работает программа:
# Исходный список: Список чисел сохраняется в переменной my_list.

# Начальный индекс
index = 0

# Как работает программа:
# Инициализация индекса: Переменная index устанавливается в 0, чтобы начать перебор с первого элемента списка.

# Цикл while для обхода списка
while index < len(my_list):
    number = my_list[index]

    # Как работает программа:
    # Цикл while:
    # Условие цикла проверяет, что текущий индекс меньше длины списка (index < len(my_list)).
    # Внутри цикла текущее число извлекается из списка с помощью индекса.
    
    # Проверка на отрицательное число
    if number < 0:
        break  # Прерываем цикл при встрече отрицательного числа

        # Как работает программа:
        # Проверка на отрицательное число: Если текущее число меньше 0, используется оператор break для выхода из цикла.
    
    # Пропускаем ноль
    if number == 0:
        index += 1
        continue  # Продолжаем итерацию, если число равно нулю

        # Как работает программа:
        # Пропуск нуля: Если текущее число равно 0, индекс увеличивается на 1, и срабатывает оператор continue, который возвращает к началу цикла, игнорируя последующий код.
    
    # Вывод положительного числа
    print(number, end=' ')

    # Как работает программа:
    # Вывод положительных чисел: Если число положительное, оно выводится на экран, и индекс увеличивается на 1 для перехода к следующему элементу.
    
    # Увеличиваем индекс для следующей итерации
    index += 1
